#include <stdio.h>

int main() {
    int num;
    printf("Enter a Number: ");
    scanf("%d", &num);

    if (num > 0) {
        printf("%d is a Positive Number.\n", num);
    }
    else if(num==0) {
        printf("%d is neither Positive nor Negative.\n", num);
    }
    else {
        printf("%d is not a Positive Number.\n", num);
    }

    return 0;
}
